DROP TABLE IF EXISTS network_events;

CREATE TABLE network_events (
    event_id INTEGER PRIMARY KEY,
    timestamp DATETIME,
    source_ip TEXT,
    destination_ip TEXT,
    protocol TEXT,
    port INTEGER,
    packet_size INTEGER,
    latency_ms INTEGER,
    status_code TEXT,
    router_id TEXT,
    bandwidth_mbps INTEGER,
    error_rate DECIMAL(4,2),
    packets_dropped INTEGER,
    location TEXT
);

